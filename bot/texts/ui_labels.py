def lbl(lang_or_user, key: str, group: str = "inline") -> str:
    lang = normalize_lang(lang_or_user if isinstance(lang_or_user, str) else lang_of(lang_or_user))
    bucket = MENU if group == "menu" else INLINE
    row = bucket.get(key) or {}
    return row.get(lang) or row.get(DEFAULT_LANG) or key


def category_label(name: str, lang_or_user, emoji: str = "") -> str:
    """Локализованная подпись категории по каноническому имени из БД."""
    lang = normalize_lang(lang_or_user if isinstance(lang_or_user, str) else lang_of(lang_or_user))
    row = EVENT_CATEGORIES.get(name) or {}
    label = row.get(lang) or row.get(DEFAULT_LANG) or name
    emoji = (emoji or "").strip()
    return f"{emoji} {label}".strip() if emoji else label


def localize_stored_category(raw: str | None, lang_or_user) -> str:
    """Перевести category с карточки события (имя или «emoji имя»)."""
    lang = normalize_lang(lang_or_user if isinstance(lang_or_user, str) else lang_of(lang_or_user))
    if not raw or not str(raw).strip():
        return tx(lang, "EVENT_DEFAULT_CAT")
    text = str(raw).strip()
    if text in EVENT_CATEGORIES:
        return category_label(text, lang)
    # Сортируем по длине, чтобы «На хату» не перебивалось короче
    for name in sorted(EVENT_CATEGORIES.keys(), key=len, reverse=True):
        if text == name or text.endswith(name) or text.endswith(f" {name}"):
            prefix = text[: -len(name)].strip()
            return category_label(name, lang, emoji=prefix)
    return text


def tx(lang_or_user, key: str, **kwargs) -> str:
    """Доп. UI-тексты (не из основного TEXTS)."""
    from bot.texts.i18n import t as base_t

    lang = normalize_lang(lang_or_user if isinstance(lang_or_user, str) else lang_of(lang_or_user))
    text = (EXTRA_TEXTS.get(lang) or {}).get(key) or (EXTRA_TEXTS.get(DEFAULT_LANG) or {}).get(key)
    if text:
        try:
            return text.format(**kwargs) if kwargs else text
        except (KeyError, ValueError):
            return text
    return base_t(lang, key, **kwargs)


def all_menu_labels(action: str) -> set[str]:
    row = MENU.get(action) or {}
    labels = set(row.values())
    labels |= _MENU_LEGACY.get(action, set())
    return labels


def all_main_menu_texts() -> set[str]:
    """Все подписи reply-кнопок главного меню (для роутинга и throttle)."""
    texts: set[str] = set()
    for action in ("rate", "events", "profile", "goals", "luma", "menu"):
        texts |= all_menu_labels(action)
    return texts


