def service_err(lang_or_user, err: BaseException | str) -> str:
    """Локализовать типичные ValueError из сервисов."""
    raw = str(err)
    key = _SERVICE_ERR_MAP.get(raw)
    if key:
        return tx(lang_or_user, key)
    if raw.startswith("Минимальная сумма вывода"):
        return tx(lang_or_user, "WITHDRAW_MIN", min=raw.split("—")[-1].strip().split()[0] if "—" in raw else "")
    return raw
